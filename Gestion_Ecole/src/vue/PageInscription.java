    public void executeSQLQuery(String query, String message) throws SQLException{
        
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/gestion_ecole", "root", "");
        Statement stmt;
        try{
            
            stmt = con.createStatement();
            if(stmt.executeUpdate  (query) == 1){
                
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                affichage_table();
                JOptionPane.showMessageDialog(null, "Data " + message + " Reussie");
            }else{
                
                JOptionPane.showMessageDialog(null, "Data Not " + message);
            }
        }catch(Exception ex){
            
            ex.printStackTrace();
        }
    } 
    
