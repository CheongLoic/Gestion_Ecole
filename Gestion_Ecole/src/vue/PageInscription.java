    public void affichage_table() throws SQLException, ClassNotFoundException{
        
        ArrayList<Personne> list = getpersonnelist();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[7];
        for(int i = 0; i< list.size(); i++){
            
            row[0] = list.get(i).get_ID_Personne();
            System.out.println("row1: "+list.get(i).get_Nom_Personne());
            row[1] = list.get(i).get_Nom_Personne();
            row[2] = list.get(i).get_Prenom_Personne(); 
            row[3] = list.get(i).getCivilite();
            row[4] = list.get(i).get_Type_Personne();
            row[5] = list.get(i).getLogin();
            row[6] = list.get(i).getMdp();
            
            model.addRow(row);
            
        }
    }
    
  
 
    

