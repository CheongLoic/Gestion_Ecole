     public ArrayList<Personne> getpersonnelist() throws SQLException, ClassNotFoundException{
        
        ArrayList<Personne> personnelist = new ArrayList<Personne>();
        
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/gestion_ecole", "root", "");
        
        String query = "SELECT * FROM `personne`";
        
        ResultSet rset;
        
        try{
        //Class.forName("com.mysql.jdbc.Driver");
        Class.forName("com.mysql.cj.jdbc.Driver");
            stmt = connection.createStatement();
            rset = stmt.executeQuery(query);
            //System.out.println("rset : "+ rset); //return : com.mysql.cj.jdbc.result.ResultSetImpl@403e5912
            Personne p;
            while(rset.next()){
                p = new Personne(rset.getInt("id_personne"), rset.getString("nom_personne"), rset.getString("prenom_personne"), rset.getString("civilite"), rset.getString("type_personne"), rset.getString("Identifiant"), rset.getString("Mdp"));
                personnelist.add(p);
            }
        
        }catch (ClassNotFoundException | SQLException ex) {
                       ex.printStackTrace();
                    }
        
        return personnelist;
        
    }
    
